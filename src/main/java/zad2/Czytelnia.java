package zad2;

public class Czytelnia {
    private int ap, ac, dp, dc;
    public Czytelnia(int ap, int ac, int dp, int dc) {
        this.ap = ap;
        this.ac = ac;
        this.dp = dp;
        this.dc = dc;
    }
    public int getAp() {
        return ap;
    }
    public void setAp(int ap) {
        this.ap = ap;
    }
    public int getAc() {
        return ac;
    }
    public void setAc(int ac) {
        this.ac = ac;
    }
    public int getDp() {
        return dp;
    }
    public void setDp(int dp) {
        this.dp = dp;
    }
    public int getDc() {
        return dc;
    }
    public void setDc(int dc) {
        this.dc = dc;
    }
}